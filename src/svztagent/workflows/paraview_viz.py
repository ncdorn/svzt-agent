def _style_volume_opacity(field, lut, scalar_range=None):
    """Use a linear min-transparent to max-opaque ramp for volume rendering."""
    from paraview.simple import GetOpacityTransferFunction

    sr = scalar_range or _lut_scalar_range(lut)
    if not sr:
        return
    otf = GetOpacityTransferFunction(field)
    otf.Points = [sr[0], 0.0, 0.5, 0.0, sr[1], 1.0, 0.5, 0.0]


def _warp_by_displacement(source):
    """Apply WarpByVector using DISPLACEMENT_FIELD.

    Returns the warped pipeline object, or the original source if the
    displacement field is absent (with a warning).
    """
    from paraview.simple import WarpByVector
    if not _has_point_array(source, DISPLACEMENT_FIELD):
        print(f"[pviz] WARNING: '{DISPLACEMENT_FIELD}' not found — skipping displacement warp")
        return source
    w = WarpByVector(Input=source)
    w.Vectors = ["POINTS", DISPLACEMENT_FIELD]
    w.ScaleFactor = 1.0
    print(f"[pviz] Warping geometry by '{DISPLACEMENT_FIELD}'")
    return w


def _render_field(source, field, view, out_path, cmap, label, representation="Surface"):
    """Render a single field to a PNG.

    representation: "Surface" (extract outer shell, good for pressure/WSS)
                    "Volume"  (ray-cast through the 3-D mesh, good for velocity)
    """
    from paraview.simple import (
        ColorBy,
        Delete,
        ExtractSurface,
        GetColorTransferFunction,
        GetScalarBar,
        Hide,
        SaveScreenshot,
        Show,
    )

    surf = None
    if representation == "Surface":
        surf = ExtractSurface(Input=source)
        render_obj = Show(surf, view)
    else:
        render_obj = Show(source, view)

    render_obj.Representation = representation
    ColorBy(render_obj, ("POINTS", field))
    render_obj.RescaleTransferFunctionToDataRange(False, False)
    lut = GetColorTransferFunction(field)
    lut.ApplyPreset(cmap, True)

    if representation == "Volume":
        # Keep internal flow structure visible without obscuring the vessel wall.
        _style_volume_opacity(field, lut)

    sb = GetScalarBar(lut, view)
    _style_scalar_bar(sb, label)
    view.Update()
    SaveScreenshot(str(out_path), view, TransparentBackground=1)

    if surf is not None:
        Hide(surf, view)
        Delete(surf)
    else:
        Hide(source, view)
    return str(out_path)


def _render_animation_pass(cycle_files, view, field, cmap, label, scalar_range, frames_dir, prefix,
                            representation="Surface", calc_expr=None, calc_result=None):
    """Render each last-cycle VTU frame for one field and return list of PNG paths.

    Builds a persistent pipeline (reader → warp → [calculator] → [extract surface])
    and updates only the reader filename per frame for efficiency.

    representation: "Surface" or "Volume"
    calc_expr:      optional ParaView Calculator expression applied after warp
                    (e.g. 'Pressure / 1333.22' for mmHg, or 'mag(Velocity)' for magnitude)
    calc_result:    result array name written by the calculator
    """
    from paraview.simple import (
        Calculator, ColorBy, Delete, ExtractSurface, GetColorTransferFunction,
        GetOpacityTransferFunction, GetScalarBar, Hide, SaveScreenshot, Show,
        XMLUnstructuredGridReader,
    )

    if not cycle_files:
        return []

    Path(frames_dir).mkdir(parents=True, exist_ok=True)

    # --- Build persistent pipeline ---
    reader = XMLUnstructuredGridReader(FileName=[str(cycle_files[0])])
    reader.UpdatePipeline()

    # Warp by displacement (CMM simulations keep the displacement field in each VTU)
    warped = _warp_by_displacement(reader)

    # Optional field transformation (unit conversion or vector → scalar magnitude)
    if calc_expr and calc_result:
        calc = Calculator(Input=warped)
        calc.Function = calc_expr
        calc.ResultArrayName = calc_result
        pipeline_end = calc
        render_field = calc_result
    else:
        pipeline_end = warped
        render_field = field

    # Surface vs. volume display
    if representation == "Surface":
        surf = ExtractSurface(Input=pipeline_end)
        disp_src = surf
    else:
        disp_src = pipeline_end
        surf = None

    disp = Show(disp_src, view)
    disp.Representation = representation
    ColorBy(disp, ("POINTS", render_field))

    lut = GetColorTransferFunction(render_field)
    lut.ApplyPreset(cmap, True)
    if scalar_range:
        lut.RescaleTransferFunction(scalar_range[0], scalar_range[1])
    else:
        disp.RescaleTransferFunctionToDataRange(False, False)

    if representation == "Volume":
        # Linear opacity ramp: transparent at min → opaque at max.
        _style_volume_opacity(render_field, lut, scalar_range)

    sb = GetScalarBar(lut, view)
    _style_scalar_bar(sb, label)

    frame_paths = []
    for i, vtu in enumerate(cycle_files):
        reader.FileName = [str(vtu)]
        reader.UpdatePipeline()
        view.Update()
        fp = Path(frames_dir) / f"{prefix}_{i:04d}.png"
        SaveScreenshot(str(fp), view, TransparentBackground=1)
        frame_paths.append(fp)
        if (i + 1) % 20 == 0 or (i + 1) == len(cycle_files):
            print(f"[pviz]   {prefix} animation: {i+1}/{len(cycle_files)} frames")

    Hide(disp_src, view)
    if surf is not None:
        Delete(surf)
    Delete(reader)
    return frame_paths


def _assemble_animation(frame_paths, prefix, out_dir, fps=20):
    """Combine PNG frames into an MP4 (ffmpeg) or GIF (imageio) animation.

    Falls back gracefully if neither tool is available, leaving the PNG frames
    in place and printing the manual ffmpeg command.

    Returns the path of the assembled file, or the frames directory on fallback.
    """
    import subprocess

    if not frame_paths:
        return None

    frames_dir = frame_paths[0].parent
    # ffmpeg printf-style input pattern (frames are 0-indexed, zero-padded to 4 digits)
    frame_pattern = str(frames_dir / f"{prefix}_%04d.png")
    mp4_path = Path(out_dir) / f"{prefix}_animation.mp4"

    try:
        result = subprocess.run(
            ["ffmpeg", "-y",
             "-framerate", str(fps),
             "-i", frame_pattern,
             "-c:v", "mpeg4", "-pix_fmt", "yuv420p", "-q:v", "5",
             str(mp4_path)],
            capture_output=True, timeout=300,
        )
        if result.returncode == 0:
            print(f"[pviz] Animation (MP4): {mp4_path}")
            return str(mp4_path)
        print(f"[pviz] ffmpeg error: {result.stderr.decode()[:300]}", file=sys.stderr)
    except FileNotFoundError:
        print(f"[pviz] ffmpeg not found — trying imageio GIF")
    except Exception as exc:
        print(f"[pviz] ffmpeg failed: {exc}", file=sys.stderr)

    # Fallback: animated GIF via imageio
    gif_path = Path(out_dir) / f"{prefix}_animation.gif"
    try:
        import imageio
        with imageio.get_writer(str(gif_path), mode="I", fps=fps, loop=0) as writer:
            for fp in frame_paths:
                writer.append_data(imageio.imread(str(fp)))
        print(f"[pviz] Animation (GIF): {gif_path}")
        return str(gif_path)
    except ImportError:
        pass
    except Exception as exc:
        print(f"[pviz] imageio GIF failed: {exc}", file=sys.stderr)

    # Last resort: leave frames and print manual command
    manual_cmd = f"ffmpeg -framerate {fps} -i {frame_pattern} -c:v mpeg4 -pix_fmt yuv420p -q:v 5 {mp4_path}"
    print(f"[pviz] Frames saved to: {frames_dir}")
    print(f"[pviz] Assemble manually: {manual_cmd}")
    return str(frames_dir)


