def _resolve_viz_config(
    base: ParaViewVizConfig,
    override: PatientParaViewVizOverride | None,
) -> ParaViewVizConfig:
    """Merge patient-level paraview_viz overrides on top of workspace defaults.

    Only fields explicitly set on the override (non-None) replace the base value.
    This means a patient only needs to specify camera_offset_dir / camera_view_up
    and inherits mem, wall_time_hours, image_resolution, field names from defaults.
    """
    if override is None:
        return base
    updates = override.model_dump(exclude_none=True)
    return base.model_copy(update=updates)


def _resolve_patient_viz_settings(config, manifest) -> tuple[ParaViewVizConfig, float, str | None]:
    patient_alias: str | None = manifest.patient.get("alias") if manifest.patient else None
    patient_cfg = None
    patient_override = None
    if patient_alias:
        patient_cfg = next((p for p in config.patients if p.alias == patient_alias), None)
        if patient_cfg is not None and patient_cfg.postprocess is not None:
            patient_override = patient_cfg.postprocess.paraview_viz
    viz_cfg = _resolve_viz_config(config.defaults.postprocess.paraview_viz, patient_override)

    threed_cfg = config.defaults.tuning.threed
    dt = threed_cfg.dt
    if patient_cfg is not None and patient_cfg.tuning is not None:
        if patient_cfg.tuning.threed is not None and patient_cfg.tuning.threed.dt is not None:
            dt = patient_cfg.tuning.threed.dt
    return viz_cfg, dt, patient_alias


