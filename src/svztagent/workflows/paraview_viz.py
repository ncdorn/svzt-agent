def _prepare_paraview_viz_job(
    *,
    workspace_root: Path,
    run_id: str,
    manifest,
    cluster,
    source_iteration: int,
    stage: str,
    local_paths: dict[str, Path],
    remote_paths: dict[str, str],
    cycle_duration_s: float,
    job_name: str,
) -> PreparedParaViewVizJob:
    config = load_workspace_config(workspace_root)
    if cluster.executables.pvpython_path is None:
        raise ConfigError(
            "cluster executables.pvpython_path is required for ParaView visualization; "
            "set it in clusters.yaml"
        )

    viz_cfg, dt, _patient_alias = _resolve_patient_viz_settings(config, manifest)
    for key in ("root", "logs"):
        local_paths[key].mkdir(parents=True, exist_ok=True)

    pvpython_script = _render_pvpython_script(
        simulation_dir=remote_paths["simulation_dir"],
        output_dir=remote_paths["output_dir"],
        cycle_duration_s=cycle_duration_s,
        dt=dt,
        image_resolution=viz_cfg.image_resolution,
        camera_offset_dir=viz_cfg.camera_offset_dir,
        camera_view_up=viz_cfg.camera_view_up,
        pressure_field=viz_cfg.pressure_field,
        velocity_field=viz_cfg.velocity_field,
        wss_field=viz_cfg.wss_field,
        displacement_field=viz_cfg.displacement_field,
    )
    local_paths["pvpython_script"].write_text(pvpython_script, encoding="utf-8")

    slurm_script = _render_slurm_script(
        pvpython_path=cluster.executables.pvpython_path,
        remote_pvpython_script=remote_paths["pvpython_script"],
        remote_root=remote_paths["root"],
        remote_logs_dir=remote_paths["logs"],
        output_dir=remote_paths["output_dir"],
        account=config.defaults.scheduler.account,
        partition=config.defaults.scheduler.partition,
        wall_time_hours=viz_cfg.wall_time_hours,
        cpus_per_task=viz_cfg.cpus,
        mem=viz_cfg.mem,
    )
    local_paths["slurm_script"].write_text(slurm_script, encoding="utf-8")

    return PreparedParaViewVizJob(
        run_id=run_id,
        stage=stage,
        source_iteration=source_iteration,
        local_root=local_paths["root"],
        local_logs_dir=local_paths["logs"],
        local_pvpython_script=local_paths["pvpython_script"],
        local_slurm_script=local_paths["slurm_script"],
        remote_root=remote_paths["root"],
        remote_logs_dir=remote_paths["logs"],
        remote_pvpython_script=remote_paths["pvpython_script"],
        remote_slurm_script=remote_paths["slurm_script"],
        remote_output_dir=remote_paths["output_dir"],
        remote_simulation_dir=remote_paths["simulation_dir"],
        remote_submission_metadata_path=str(
            PurePosixPath(remote_paths["output_dir"]) / "paraview_viz_submission.json"
        ),
        pvpython_script_content=pvpython_script,
        slurm_script_content=slurm_script,
        submit_options=SlurmSubmitOptions(
            job_name=job_name,
            account=config.defaults.scheduler.account,
            partition=config.defaults.scheduler.partition,
            wall_time=f"{viz_cfg.wall_time_hours}:00:00",
            mem=viz_cfg.mem,
            cpus=str(viz_cfg.cpus),
        ),
    )


def _paraview_skip_reason(*, config, cluster, manifest) -> str | None:
    if cluster.executables.pvpython_path is None:
        return "pvpython_path_not_configured"
    viz_cfg, _dt, _patient_alias = _resolve_patient_viz_settings(config, manifest)
    if viz_cfg.cycle_duration_s is None:
        return "cycle_duration_s_not_configured"
    return None


def _prepare_preop_paraview_viz_job(
    *,
    workspace_root: Path,
    run_id: str,
    manifest,
    cluster,
    iteration: int,
    cycle_duration_s: float,
) -> PreparedParaViewVizJob:
    remote_run_dir = manifest.execution.remote_run_dir or manifest.remote.get("remote_run_dir")
    if not remote_run_dir:
        raise ConfigError(f"run '{run_id}' is missing remote_run_dir")
    return _prepare_paraview_viz_job(
        workspace_root=workspace_root,
        run_id=run_id,
        manifest=manifest,
        cluster=cluster,
        source_iteration=iteration,
        stage="preop",
        local_paths=_preop_local_paths(workspace_root, run_id, iteration),
        remote_paths=_preop_remote_paths(str(remote_run_dir), iteration),
        cycle_duration_s=cycle_duration_s,
        job_name=f"{run_id}-pviz",
    )


def _prepare_postop_paraview_viz_job(
    *,
    workspace_root: Path,
    run_id: str,
    manifest,
    cluster,
    remote_postop_dir: str | None = None,
    source_iteration: int | None = None,
) -> PreparedParaViewVizJob:
    postop_run = manifest.postop_run
    resolved_remote_postop_dir = remote_postop_dir
    resolved_source_iteration = source_iteration
    if resolved_remote_postop_dir is None or resolved_source_iteration is None:
        if not postop_run or not postop_run.remote_dir:
            raise ConfigError(
                f"run '{run_id}' has no postop_run recorded in manifest; submit the postop simulation first"
            )
        resolved_remote_postop_dir = postop_run.remote_dir
        resolved_source_iteration = int(postop_run.source_preop_iteration)
    config = load_workspace_config(workspace_root)
    viz_cfg, _dt, patient_alias = _resolve_patient_viz_settings(config, manifest)
    if viz_cfg.cycle_duration_s is None:
        alias_note = f" under patient {patient_alias!r}" if patient_alias else ""
        raise ConfigError(
            "ParaView visualization for postop requires postprocess.paraview_viz.cycle_duration_s"
            f"{alias_note}"
        )
    return _prepare_paraview_viz_job(
        workspace_root=workspace_root,
        run_id=run_id,
        manifest=manifest,
        cluster=cluster,
        source_iteration=int(resolved_source_iteration),
        stage="postop",
        local_paths=_postop_local_paths(workspace_root, run_id, int(resolved_source_iteration)),
        remote_paths=_postop_remote_paths(str(resolved_remote_postop_dir)),
        cycle_duration_s=viz_cfg.cycle_duration_s,
        job_name=f"{run_id}-pviz-post",
    )


def _prepare_adaptation_paraview_viz_job(
    *,
    workspace_root: Path,
    run_id: str,
    manifest,
    cluster,
    model: str,
    remote_adaptation_dir: str | None = None,
    source_iteration: int | None = None,
) -> PreparedParaViewVizJob:
    adaptation_run = None
    if remote_adaptation_dir is None or source_iteration is None:
        for record in reversed(manifest.adaptation_runs):
            if record.model.upper() == model.upper():
                adaptation_run = record
                break
        if adaptation_run is None:
            raise ConfigError(
                f"run '{run_id}' has no recorded adaptation run for model '{model.upper()}'"
            )
        remote_adaptation_dir = adaptation_run.remote_dir
        source_iteration = int(adaptation_run.source_preop_iteration)

    config = load_workspace_config(workspace_root)
    viz_cfg, _dt, patient_alias = _resolve_patient_viz_settings(config, manifest)
    if viz_cfg.cycle_duration_s is None:
        alias_note = f" under patient {patient_alias!r}" if patient_alias else ""
        raise ConfigError(
            "ParaView visualization for adaptation requires postprocess.paraview_viz.cycle_duration_s"
            f"{alias_note}"
        )
    model_slug = model.lower()
    return _prepare_paraview_viz_job(
        workspace_root=workspace_root,
        run_id=run_id,
        manifest=manifest,
        cluster=cluster,
        source_iteration=int(source_iteration),
        stage=f"adaptation-{model_slug}",
        local_paths=_adaptation_local_paths(workspace_root, run_id, int(source_iteration), model_slug),
        remote_paths=_adaptation_remote_paths(str(remote_adaptation_dir)),
        cycle_duration_s=viz_cfg.cycle_duration_s,
        job_name=f"{run_id}-pviz-adapt-{model_slug}",
    )


def _submit_prepared_paraview_viz_job(
    *,
    prepared: PreparedParaViewVizJob,
    manifest_path: Path,
    cluster,
    config,
    transfer_adapter: FileTransferAdapter | None,
    scheduler_adapter: SchedulerAdapter | None,
    remote_exec_adapter: RemoteExecAdapter | None,
    manifest_note: str,
) -> ParaViewVizSubmissionResult:
    if transfer_adapter is None or scheduler_adapter is None or remote_exec_adapter is None:
        default_transfer, default_scheduler, default_remote = _build_default_adapters(
            cluster=cluster,
            config=config,
            run_id=prepared.run_id,
            mode=ExecutionMode.EXECUTE,
        )
        transfer_adapter = transfer_adapter or default_transfer
        remote_exec_adapter = remote_exec_adapter or default_remote
        if scheduler_adapter is None:
            scheduler_adapter = SlurmSchedulerAdapter(
                remote_exec=remote_exec_adapter,
                runs_root=cluster.remote_roots.runs_root,
                submit_options=prepared.submit_options,
            )

    command_results = [
        transfer_adapter.ensure_remote_dir(prepared.remote_root),
        transfer_adapter.ensure_remote_dir(prepared.remote_logs_dir),
        transfer_adapter.ensure_remote_dir(prepared.remote_output_dir),
        transfer_adapter.push(str(prepared.local_pvpython_script), prepared.remote_pvpython_script),
        transfer_adapter.push(str(prepared.local_slurm_script), prepared.remote_slurm_script),
    ]
    submit_result = scheduler_adapter.submit(prepared.remote_slurm_script)
    command_results.append(submit_result.command)

    manifest = read_manifest(manifest_path)
    manifest = record_paraview_viz_submission(
        manifest,
        stage=prepared.stage,
        source_iteration=prepared.source_iteration,
        local_dir=str(prepared.local_root),
        remote_dir=prepared.remote_output_dir,
        local_script_path=str(prepared.local_slurm_script),
        remote_script_path=prepared.remote_slurm_script,
        scheduler_job_id=submit_result.job_id,
        note=manifest_note,
    )
    write_manifest(manifest, manifest_path)

    return ParaViewVizSubmissionResult(
        run_id=prepared.run_id,
        stage=prepared.stage,
        source_iteration=prepared.source_iteration,
        remote_output_dir=prepared.remote_output_dir,
        remote_slurm_script=prepared.remote_slurm_script,
        local_slurm_script=prepared.local_slurm_script,
        submitted_job_id=submit_result.job_id,
        command_previews=[result.argv for result in command_results],
    )


