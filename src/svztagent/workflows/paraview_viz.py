def _extract_step(path):
    m = re.search(r"result_(\d+)\.vtu$", Path(path).name)
    return int(m.group(1)) if m else -1


def _find_vtu_files(sim_dir):
    sim_path = Path(sim_dir)
    files = sorted(sim_path.glob("*-procs/result_*.vtu"), key=_extract_step)
    if not files:
        files = sorted(sim_path.glob("result_*.vtu"), key=_extract_step)
    if not files:
        raise FileNotFoundError(f"No result_*.vtu files found in {sim_dir}")
    return files


def _last_cycle_files(all_files, cycle_duration_s, dt):
    """Return files whose simulation time falls within the last cardiac cycle.

    VTU files are output every N timesteps so index-based slicing is wrong —
    we filter by actual simulation time derived from each file's step number.
    """
    last_step = _extract_step(all_files[-1])
    last_time = last_step * dt
    cycle_start_time = last_time - cycle_duration_s
    # Keep files whose step number implies time >= cycle_start_time
    cycle_start_step = cycle_start_time / dt
    result = [f for f in all_files if _extract_step(f) >= cycle_start_step]
    return result if result else all_files


def _read_vtu(path):
    reader = vtk.vtkXMLUnstructuredGridReader()
    reader.SetFileName(str(path))
    reader.Update()
    mesh = vtk.vtkUnstructuredGrid()
    mesh.DeepCopy(reader.GetOutput())
    return mesh


def _point_array(mesh, *names):
    pd = mesh.GetPointData()
    for name in names:
        arr = pd.GetArray(name)
        if arr is not None:
            return arr, name
    return None, None


def _systolic_frame(files):
    """Return file with highest mean pressure in the last cardiac cycle."""
    best_path, best_p = files[0], -float("inf")
    for f in files:
        mesh = _read_vtu(f)
        arr, _ = _point_array(mesh, PRESSURE_FIELD)
        if arr is None:
            continue
        vals = vtk_to_numpy(arr)
        mean_p = float(np.mean(vals[:, 0] if vals.ndim == 2 else vals))
        if mean_p > best_p:
            best_p, best_path = mean_p, f
    return best_path


def _build_cycle_averages(files, tmp_path):
    """One sequential pass over last-cycle frames computing all cycle-averaged fields
    and global scalar ranges needed for consistent animation coloring.

    Computes and writes to tmp_path:
      MeanPressure  — cycle-averaged pressure scalar
      MeanVelocity  — cycle-averaged velocity magnitude
      TAWSS         — time-averaged WSS magnitude

    Returns a dict:
      {
        "fields": set of field names written to tmp_path,
        "scalar_ranges": {
            PRESSURE_FIELD: [global_min, global_max],  # for animation LUT
            VELOCITY_FIELD: [global_min, global_max],
        }
      }
    """
    acc_p = acc_v = acc_w = None
    n_p = n_v = n_w = 0
    ref = None
    p_min = p_max = v_min = v_max = None

    for f in files:
        mesh = _read_vtu(f)
        ref = mesh  # keep last successfully-read mesh as geometry carrier

        # Pressure (scalar)
        arr, _ = _point_array(mesh, PRESSURE_FIELD)
        if arr is not None:
            vals = vtk_to_numpy(arr)
            p = (vals[:, 0] if vals.ndim == 2 else vals).astype(np.float64)
            acc_p = p.copy() if acc_p is None else acc_p + p
            lo, hi = float(p.min()), float(p.max())
            p_min = lo if p_min is None else min(p_min, lo)
            p_max = hi if p_max is None else max(p_max, hi)
            n_p += 1

        # Velocity magnitude
        arr, _ = _point_array(mesh, VELOCITY_FIELD)
        if arr is not None:
            vals = vtk_to_numpy(arr)
            mag = (np.linalg.norm(vals, axis=1) if vals.ndim == 2 else np.abs(vals)).astype(np.float64)
            acc_v = mag.copy() if acc_v is None else acc_v + mag
            lo, hi = float(mag.min()), float(mag.max())
            v_min = lo if v_min is None else min(v_min, lo)
            v_max = hi if v_max is None else max(v_max, hi)
            n_v += 1

        # WSS magnitude (→ TAWSS)
        arr, _ = _point_array(mesh, WSS_FIELD, "vWSS")
        if arr is not None:
            vals = vtk_to_numpy(arr)
            mag = (np.linalg.norm(vals, axis=1) if vals.ndim == 2 else np.abs(vals)).astype(np.float64)
            acc_w = mag.copy() if acc_w is None else acc_w + mag
            n_w += 1

    if ref is None:
        return {"fields": set(), "scalar_ranges": {}}

    pd = ref.GetPointData()
    available = set()

    def _add(name, acc, n, scale=1.0):
        if n > 0:
            a = numpy_to_vtk((acc / n * scale).astype(np.float64), deep=True)
            a.SetName(name)
            pd.AddArray(a)
            available.add(name)

    _add("MeanPressure",  acc_p, n_p, scale=PRESSURE_TO_MMHG)  # stored in mmHg
    _add("MeanVelocity",  acc_v, n_v)
    _add("TAWSS",         acc_w, n_w)

    writer = vtk.vtkXMLUnstructuredGridWriter()
    writer.SetFileName(str(tmp_path))
    writer.SetInputData(ref)
    writer.Write()

    scalar_ranges = {}
    if p_min is not None:
        # Convert pressure range to mmHg for consistent animation LUT
        scalar_ranges[PRESSURE_FIELD] = [p_min * PRESSURE_TO_MMHG, p_max * PRESSURE_TO_MMHG]
    if v_min is not None:
        scalar_ranges[VELOCITY_FIELD] = [v_min, v_max]
    return {"fields": available, "scalar_ranges": scalar_ranges}


def _norm3(v):
    a = np.asarray(v, dtype=float)
    n = np.linalg.norm(a)
    return (a / n).tolist() if n > 1e-12 else [0.0, -1.0, 0.0]


def _setup_camera(view, bounds):
    import math
    cx = 0.5 * (bounds[0] + bounds[1])
    cy = 0.5 * (bounds[2] + bounds[3])
    cz = 0.5 * (bounds[4] + bounds[5])
    dx = bounds[1] - bounds[0]
    dy = bounds[3] - bounds[2]
    dz = bounds[5] - bounds[4]
    diag = math.sqrt(dx*dx + dy*dy + dz*dz) or 1.0
    # Camera distance: model diagonal fills ~80% of the vertical FOV (30°).
    # dist = diag / (2 * tan(15°) * 0.8) ≈ diag * 2.33
    # Do NOT call ResetCamera() — it resets to defaults when no geometry is shown yet.
    dist = diag * 2.33
    d = _norm3(CAMERA_OFFSET_DIR)
    view.CameraFocalPoint = [cx, cy, cz]
    view.CameraPosition = [cx + d[0] * dist, cy + d[1] * dist, cz + d[2] * dist]
    if CAMERA_VIEW_UP is not None:
        # Explicit view-up from config (read from ParaView GUI Python Shell).
        view.CameraViewUp = list(CAMERA_VIEW_UP)
    else:
        # Auto-select: Z-up unless camera is nearly vertical, then Y-up.
        view.CameraViewUp = [0.0, 0.0, 1.0] if abs(d[2]) < 0.9 else [0.0, 1.0, 0.0]
    print(f"[pviz] Camera: focal={[round(v,1) for v in [cx,cy,cz]]} "
          f"dist={dist:.1f} dir={[round(v,3) for v in d]}")


def _has_point_array(source, name):
    """Return True if the source pipeline output has a point data array with the given name."""
    info = source.GetDataInformation().GetPointDataInformation()
    for i in range(info.GetNumberOfArrays()):
        if info.GetArrayInformation(i).GetName() == name:
            return True
    return False


def _lut_scalar_range(lut):
    """Return the scalar range encoded in a ParaView color transfer function."""
    pts = list(getattr(lut, "RGBPoints", []) or [])
    if len(pts) >= 5:
        return [float(pts[0]), float(pts[-4])]
    return None


